    /* Métodos propios */
    private static double calcularDiscriminante(double term_cuad, double term_lineal, double constante) {
        /*
        Algoritmo que calcula el discriminante de una ecuación y retorna el resultado
         */
        // Zona de declaración de variable
        double resultado;

        resultado = (term_lineal * term_lineal) - (4 * term_cuad * constante);    // Calcula el discriminante

        return resultado;
    }

    private static String determinarRaices(String prim_raiz, String seg_raiz) {
        /*
        Algoritmo que determina la condición de las raíces para mostrar por pantalla
        las raíces con o sin decimales.        
         */
        // Zona de declaración e inicialización de variable
        String resultado;

        if (prim_raiz.length() > 6) {
            if (seg_raiz.length() > 6) {
                resultado = ("Las raíces son: " + prim_raiz.substring(0, 6) + " y " + seg_raiz.substring(0, 6));
            } else {
                resultado = ("Las raíces son: " + prim_raiz.substring(0, 6) + " y " + seg_raiz);
            }
        } else {
            if (seg_raiz.length() > 6) {
                resultado = ("Las raíces son: " + prim_raiz + " y " + seg_raiz.substring(0, 6));
            } else {
                resultado = ("Las raíces son: " + prim_raiz + " y " + seg_raiz);
            }
        }
        return resultado;
    }

