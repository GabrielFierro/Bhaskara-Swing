    private void resolveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resolveButtonActionPerformed
        // TODO add your handling code here:
        // Zona de declaración de variables
        double discriminante, termino_cuadratico, termino_lineal, constante, res, raiz_doble, primera_raiz, segunda_raiz;
        String prim_raiz, seg_raiz, raices;
        boolean continuar;
        // Zona de inicialización de variables

        if (firstComponentTextField.getText().isEmpty() || secondComponentTextField.getText().isEmpty() || thirdComponentTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,"Complete los campos vacíos");
        } else {

            termino_cuadratico = Double.parseDouble(firstComponentTextField.getText());
            termino_lineal = Double.parseDouble(secondComponentTextField.getText());
            constante = Double.parseDouble(thirdComponentTextField.getText());
            
            // Primero verifica que se trate de una función cuadrática
            if (termino_cuadratico == 0) {
                resultTextField.setText("El término cuadrático debe ser distinto a cero");
            } else {
                // Calcula el valor del discriminante
                discriminante = calcularDiscriminante(termino_cuadratico, termino_lineal, constante);

                // En relación al discriminante determina el resultado de la ecuación
                if (discriminante < 0) {
                    resultTextField.setText("No existe solución en los reales");
                } else {
                    if (discriminante == 0) {
                        raiz_doble = (-termino_lineal) / (2 * termino_cuadratico);   // Calcula la raíz
                        resultTextField.setText("La raíz doble es: " + raiz_doble);
                    } else {
                        res = Math.sqrt(discriminante); // Aplica la raíz cuadrada al discriminante
                        primera_raiz = (-termino_lineal + res) / (2 * termino_cuadratico);
                        segunda_raiz = (-termino_lineal - res) / (2 * termino_cuadratico);
                        // Casteo a string para luego mostrar los primeros 4 dígitos decimales
                        prim_raiz = String.valueOf(primera_raiz);
                        seg_raiz = String.valueOf(segunda_raiz);

                        raices = determinarRaices(prim_raiz, seg_raiz);

                        resultTextField.setText(raices);

                    }
                }
            }
        }


    }//GEN-LAST:event_resolveButtonActionPerformed

